package com.company;

public class Passenger {
    String name;
    int age;
    String gender;
    String source;
    String destination;
    Passenger(String name, int age, String gender, String source, String destination) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.source = source;
        this.destination = destination;
    }
}
